week = int(input("Informe a semana (1,2,3,4,5,6,7): "))

if week == 1:
    print("Domingo")
    pass

elif week == 2:
    print("Segunda-feira")
    pass

elif week == 3:
    print("Terça-feira")
    pass

elif week == 4:
    print("Quarta-feira")
    pass

elif week == 5:
    print("Quinta-feira")
    pass

elif week == 6:
    print("Sexta-cheira")
    pass

elif week == 7:
    print("Sabado")
    pass

else:
    print("Opção invalida!")
